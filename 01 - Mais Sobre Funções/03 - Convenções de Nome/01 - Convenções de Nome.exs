# O padrão de nomes de funções são:
# 1º) Sempre utilizar o padrão snake_case, separando as palavras de uma função com o "_";

# O padrão de nomes de arquivos são:
# 1º) Usar o padrão snake_case.

# Ao importar um módulo, caso o nome de uma função seja precedida com "_", não poderemos utilizar ela diretamente ("function_name()"), só poderemos acessa-la através do nome do módulo ("ModuleName.function_name()").

# Sempre que temos uma função que termine com "!" após o nome dela, significa que podemos lançar um erro.

